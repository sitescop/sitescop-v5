/** Local calendar date as YYYY-MM-DD (user's timezone). */
export function localDateKey(date: Date = new Date()): string {
  const y = date.getFullYear();
  const m = String(date.getMonth() + 1).padStart(2, '0');
  const d = String(date.getDate()).padStart(2, '0');
  return `${y}-${m}-${d}`;
}

export function formatLocalDateLabel(dateKey: string): string {
  const [y, m, d] = dateKey.split('-').map(Number);
  return new Date(y, m - 1, d).toLocaleDateString('en-AU', {
    weekday: 'long',
    day: 'numeric',
    month: 'long',
    year: 'numeric',
  });
}

export function eventMatchesLocalDate(eventScheduledIso: string, dateKey: string): boolean {
  const eventLocal = localDateKey(new Date(eventScheduledIso));
  return eventLocal === dateKey;
}
